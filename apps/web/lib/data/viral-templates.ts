export interface ViralTemplateData {
  category: string;
  niche: string | null;
  template: string;
  placeholders: Record<string, string>;
  exampleFilled: string;
  hookType: string;
  format: string;
  emotionalTrigger: string;
}

export type TemplateIntent =
  | "informational"
  | "engagement"
  | "authority"
  | "conversion";

export type TemplateDifficulty = "easy" | "medium" | "advanced";

export interface EnrichedViralTemplate extends ViralTemplateData {
  intent: TemplateIntent;
  difficulty: TemplateDifficulty;
  qualityScore: number;
  curated: boolean;
}

function inferIntent(template: ViralTemplateData): TemplateIntent {
  if (template.category === "cta") return "engagement";
  if (template.category === "hot-take") return "authority";
  if (template.format === "thread-starter") return "authority";
  if (template.hookType === "question") return "engagement";
  if (template.emotionalTrigger === "aspiration") return "conversion";
  return "informational";
}

function inferDifficulty(template: ViralTemplateData): TemplateDifficulty {
  const placeholderCount = Object.keys(template.placeholders).length;
  if (placeholderCount >= 5 || template.format === "thread-starter") return "advanced";
  if (placeholderCount >= 3) return "medium";
  return "easy";
}

function inferQualityScore(template: ViralTemplateData): number {
  const base = 72;
  const emotionalBonus = ["curiosity", "controversy", "aspiration", "urgency"].includes(
    template.emotionalTrigger
  )
    ? 8
    : 4;
  const structureBonus = template.format === "thread-starter" ? 7 : template.format === "listicle" ? 5 : 3;
  const specificityBonus = Math.min(8, Object.keys(template.placeholders).length * 2);
  return Math.min(98, base + emotionalBonus + structureBonus + specificityBonus);
}

function enrichTemplate(template: ViralTemplateData): EnrichedViralTemplate {
  const qualityScore = inferQualityScore(template);
  return {
    ...template,
    intent: inferIntent(template),
    difficulty: inferDifficulty(template),
    qualityScore,
    curated: qualityScore >= 82,
  };
}

