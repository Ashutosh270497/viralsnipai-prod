export function getHooksByCategory(category: HookExample['category']): HookExample[] {
  return HOOK_LIBRARY.filter((hook) => hook.category === category);
}

export function getHooksByNiche(niche: string): HookExample[] {
  return HOOK_LIBRARY.filter((hook) => !hook.niche || hook.niche === niche);
}

export function getHighPerformanceHooks(): HookExample[] {
  return HOOK_LIBRARY.filter((hook) => hook.performance === 'high');
}

export function getRandomHook(options?: {
  category?: HookExample['category'];
  niche?: string;
  performance?: 'high' | 'medium' | 'low';
}): HookExample {
  let filtered = HOOK_LIBRARY;

  if (options?.category) {
    filtered = filtered.filter((h) => h.category === options.category);
  }
  if (options?.niche) {
    filtered = filtered.filter((h) => !h.niche || h.niche === options.niche);
  }
  if (options?.performance) {
    filtered = filtered.filter((h) => h.performance === options.performance);
  }

  return filtered[Math.floor(Math.random() * filtered.length)] || HOOK_LIBRARY[0];
}
